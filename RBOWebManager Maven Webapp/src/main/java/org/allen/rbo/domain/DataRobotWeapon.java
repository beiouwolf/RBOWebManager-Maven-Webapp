package org.allen.rbo.domain;

public class DataRobotWeapon {
	private int robotid;
	private String name;
	private String power;
	private String def;
	private String en;
	private String space;
	private String crtical;
	private String speed1;
	private String speed2;
	private String land;
	private String attr;
	private String ammo;
	public int getRobotid() {
		return robotid;
	}
	public void setRobotid(int robotid) {
		this.robotid = robotid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPower() {
		return power;
	}
	public void setPower(String power) {
		this.power = power;
	}
	public String getDef() {
		return def;
	}
	public void setDef(String def) {
		this.def = def;
	}
	public String getEn() {
		return en;
	}
	public void setEn(String en) {
		this.en = en;
	}
	public String getSpace() {
		return space;
	}
	public void setSpace(String space) {
		this.space = space;
	}
	public String getCrtical() {
		return crtical;
	}
	public void setCrtical(String crtical) {
		this.crtical = crtical;
	}
	public String getSpeed1() {
		return speed1;
	}
	public void setSpeed1(String speed1) {
		this.speed1 = speed1;
	}
	public String getSpeed2() {
		return speed2;
	}
	public void setSpeed2(String speed2) {
		this.speed2 = speed2;
	}
	public String getLand() {
		return land;
	}
	public void setLand(String land) {
		this.land = land;
	}
	public String getAttr() {
		return attr;
	}
	public void setAttr(String attr) {
		this.attr = attr;
	}
	public String getAmmo() {
		return ammo;
	}
	public void setAmmo(String ammo) {
		this.ammo = ammo;
	}
}
